#LOGICAL OPERATOR :
"""
OR : ONE TRUE CONDITION IS TRUE 
AND : ONE FALSE CONDITION IS FALSE 
NOT : WE WANT TO CHANGE THE CONDITION TO OPOSITE 
"""

A = True
B = False 


print(A or A)
print(A or B)
print(B or A)
print(B or B)

#and
print(A and A)
print(A and B)
print(B and A)
print(B and B)

#not 
print(not(True))
print(not(False))