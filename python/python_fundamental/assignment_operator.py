#ASSIGNMENT OPERATOR :-
"""
= : assign the value to the variable
example = a = 10 (now a contain 10)

+= :
-= :
*= :
/= :
%= : 
//= :
**= :

"""

a = 10
print(a)
#10

a += 10
print(a)
#20

a -= 10
print(a)
#10

a *= 10
print(a)
#100

a /= 10
print(a)
#10

a **= 10
print(a)
#a to the power 10

b = 20
b //= 10
print(b)
# remainder

c = 10
c /= 5
print(c)

c = 5
c %= 10
print(c)
#ignore the decimal value



