#TYPECASTING: converting one data type to another 
#syntax: variable(new) = want to convert in data types(name of var in which that data types is in ) 
a = 23
print(a)
print(type(a)) # this is for which types of data is in var a 
# converting int(data type) into another str(data types )
b = str(a)
print(b)
print(type(b))

c = 3.14
print(c)
print(type(c))
#converting to int
d = int(c)
print(d)
print(type(d))

